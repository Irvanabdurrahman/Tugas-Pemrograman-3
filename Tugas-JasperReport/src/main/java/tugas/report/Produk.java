package tugas.report;


public class Produk{
	private String kode;
	private String nama;
	private String harga;
	
	public String getKode(){
		return kode;
	}
	
	public void setKode(String kode){
		this.kode=kode;
	}
	
	public String getNama(){
		return nama;
	}
	
	public void setNama(String nama){
		this.nama = nama;
	}
	
	public String getHarga(){
		return harga;
	}
	
	public void setHarga(String harga){
		this.harga=harga;
	}
}